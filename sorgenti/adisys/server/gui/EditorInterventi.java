	/**
	 * Prepara il modulo per l'inserimento di un nuovo intervento.
	 */
	private void setModalitaAggiungi() {

		// RESET DEGLI INDICI DI PAZIENTE ED INFERMIERE
		// txModalita.setText(modAggiungi);
		flagModalita = modAggiungi;

		// txInterventoSelezionato.setText(null);
		flagInterventoSelezionato = null;
		txInfermiereSelezionato.setText(null);
		txPazienteSelezionato.setText(null);

		// RESET SELEZIONI TABELLA
		tabellaPazienti.clearSelection();
		tabellaInfermieri.clearSelection();
		tabellaInterventi.clearSelection();

		// RESET DEI CAMPI DI INSERIMENTO
		txData.setText("");
		txOra.setText("");

		txCAP.setText("");
		txCitta.setText("");
		txCivico.setText("");

		// RESET ETICHETTE
		txNomePaziente.setText(Fc.MessageBundle("MsgDialog520"));
		txNomeInfermiere.setText(Fc.MessageBundle("MsgDialog530"));
		labelIntervento.setText(Fc.MessageBundle("MsgDialog540"));
		pulsanteConferma.setText(Fc.MessageBundle("MsgDialog550"));

		// Reset tipi Intervento
		String[] colonne = Fc
				.getTableHeaderNames("JTableEditorInterventi-Patologie");
		DefaultTableModel modelloTipi = new DefaultTableModel(colonne, 0);
		modelloTipiIntervento = modelloTipi;
		tabellaTipiIntervento.setModel(modelloTipiIntervento);

	}

	/**
	 * Se c'e' un intervento selezionato prepara il modulo per la modifica,
	 * altrimenti visualizza un messaggio di errore.
	 */
	private void setModalitaModifica() {
		// Se e' selezionato un valore nella tabella interventi prepara il
		// modulo
		// per la modifica. Altrimenti visualizza un messaggio di errore

		// VERIFICA INTERVENTO SELEZIONATO
		int rigaSelezionata = tabellaInterventi.getSelectedRow();

		if (rigaSelezionata == -1) {
			// NESSUNA RIGA SELEZIONATA
			JOptionPane.showMessageDialog(
					null,
					Fc.MessageBundle("MsgDialog130") + "\n"
							+ Fc.MessageBundle("MsgDialog65"),
					Fc.MessageBundle("MsgDialog6"), JOptionPane.ERROR_MESSAGE);
		} else {
			// RECUPERO INFO COLONNE
			String[] colonne = Fc
					.getTableHeaderNames("JTableEditorInterventi-Interventi");
			int colonnaID = tabellaInterventi.getColumn(colonne[0])
					.getModelIndex();
			int colonnaIDPaziente = tabellaInterventi.getColumnModel()
					.getColumnIndex(colonne[1]);
			int colonnaIDInfermiere = tabellaInterventi.getColumnModel()
					.getColumnIndex(colonne[2]);

			int colonnaData = tabellaInterventi.getColumnModel()
					.getColumnIndex(colonne[6]);
			int colonnaOraInizio = tabellaInterventi.getColumnModel()
					.getColumnIndex(colonne[7]);

			int colonnaCitta = tabellaInterventi.getColumnModel()
					.getColumnIndex(colonne[3]);
			int colonnaCivico = tabellaInterventi.getColumnModel()
					.getColumnIndex(colonne[4]);
			int colonnaCAP = tabellaInterventi.getColumnModel().getColumnIndex(
					colonne[5]);

			// RECUPERO INFORMAZIONI ATTUALI E POPOLAMENTO CAMPI
			// txInterventoSelezionato.setText(tabellaInterventi.getValueAt(rigaSelezionata,
			// colonnaID).toString());
			flagInterventoSelezionato = (tabellaInterventi.getValueAt(
					rigaSelezionata, colonnaID).toString());
			txPazienteSelezionato.setText(tabellaInterventi.getValueAt(
					rigaSelezionata, colonnaIDPaziente).toString());
			txInfermiereSelezionato.setText(tabellaInterventi.getValueAt(
					rigaSelezionata, colonnaIDInfermiere).toString());

			txCitta.setText(tabellaInterventi.getValueAt(rigaSelezionata,
					colonnaCitta).toString());
			txCivico.setText(tabellaInterventi.getValueAt(rigaSelezionata,
					colonnaCivico).toString());
			txCAP.setText(tabellaInterventi.getValueAt(rigaSelezionata,
					colonnaCAP).toString());

			// Elaborazione data-ora
			// Cambiamento formato data e ora
			String testoData = DateFormatConverter.cambiaFormato(
					tabellaInterventi.getValueAt(rigaSelezionata, colonnaData)
							.toString(), "yyyy-MM-dd", "dd/MM/yyyy");
			String testoOra = DateFormatConverter.cambiaFormato(
					tabellaInterventi.getValueAt(rigaSelezionata,
							colonnaOraInizio).toString(), "HH:mm:ss", "HH.mm");

			// Trace
			System.out.println(Fc.MessageBundle("MsgDialog560") + (" ")
					+ testoData);
			System.out.println(Fc.MessageBundle("MsgDialog570") + (" ")
					+ testoOra);

			// Assegnazione dataOra
			txData.setText(testoData);
			txOra.setText(testoOra);

			String[] colonnePatologie = Fc
					.getTableHeaderNames("JTableEditorInterventi-Patologie");

			ArrayList<ArrayList<Object>> parametri = new ArrayList<>();
			ArrayList<Object> par = new ArrayList<>();

			par.add("int");
			par.add((Object) Integer.valueOf(tabellaInterventi.getValueAt(
					rigaSelezionata, colonnaID).toString()));
			parametri.add(par);

			par = new ArrayList<>();
			par.add("String[]");
			par.add((Object) colonnePatologie);
			parametri.add(par);

			modelloTipiIntervento = (DefaultTableModel) iFc.processRequest(
					"getTipiIntervento", parametri);

			// modelloTipiIntervento =
			// GestioneInterventi.getTipiIntervento(Integer.valueOf(tabellaInterventi.getValueAt(rigaSelezionata,
			// colonnaID).toString()), colonnePatologie);
			tabellaTipiIntervento.setModel(modelloTipiIntervento);

			// RESET ETICHETTE
			txNomePaziente.setText(Fc.MessageBundle("MsgDialog580"));
			txNomeInfermiere.setText(Fc.MessageBundle("MsgDialog590"));
			labelIntervento.setText(Fc.MessageBundle("MsgDialog600") + (" ")
					+ flagInterventoSelezionato);
			pulsanteConferma.setText(Fc.MessageBundle("ConfirmMsg250"));

			// Settaggio variabile di stato
			// txModalita.setText(modModifica);
			flagModalita = modModifica;
		}
	}

	/**
	 * Fields are empty.
	 * 
	 * @return true, if successful
	 */
	private boolean fieldsAreEmpty() {
		if (txData.getText().isEmpty() && txOra.getText().isEmpty()
				&& txCitta.getText().isEmpty() && txCAP.getText().isEmpty()
				&& txCivico.getText().isEmpty()) {
			return true;
		} else {
			return false;
		}
	}

