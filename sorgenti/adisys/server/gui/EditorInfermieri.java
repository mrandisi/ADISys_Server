	/**
	 * Aggiorna tabella.
	 */
	public void aggiornaTabella() {
		// Popolamento dati
		@SuppressWarnings("unchecked")
		ArrayList<TOInfermiere> listaInfermieri = (ArrayList<TOInfermiere>) iFc
				.processRequest("getTabellaInfermieri",
						new ArrayList<ArrayList<Object>>());
		TOInfermiere toInfermiere;
		DefaultTableModel model = new DefaultTableModel();

		// Assegnazione nomi colonne tabella
		String[] headers = Fc.getTableHeaderNames("JTableEditorInfermieri");
		model.setColumnIdentifiers(headers);

		// inserimento dei transfer objects nella tabella
		int tLength = listaInfermieri.size();
		for (int i = 0; i < tLength; i++) {
			toInfermiere = listaInfermieri.get(i);
			String[] row = { String.valueOf(toInfermiere.getID()),
					toInfermiere.getNome(), toInfermiere.getCognome() };
			model.addRow(row);
		}
		tabellaInfermieri.setModel(model);
	}

	/**
	 * Sets the aggiungi infermiere.
	 */
	private void setAggiungiInfermiere() {
		// Setta la variabile modalita
		modalita = Modalita.aggiungi;

		// Setta il modulo per l'inserimento
		txID.setText("");
		txNome.setText("");
		txCognome.setText("");
		txInfo.setText(Fc.MessageBundle("MsgDialog500"));
	}

	/**
	 * Sets the modifica infermiere.
	 */
	private void setModificaInfermiere() {

		if (tabellaInfermieri.getSelectedRow() == -1) {
			JOptionPane.showMessageDialog(
					null,
					Fc.MessageBundle("MsgDialog185") + "\n"
							+ Fc.MessageBundle("MsgDialog60"),
					Fc.MessageBundle("MsgDialog6"), JOptionPane.ERROR_MESSAGE);

		} else {

			// Setta la variabile modalita
			modalita = Modalita.modifica;

			// Recupero valori ID NOME COGNOME
			String[] colonne = Fc.getTableHeaderNames("JTableEditorInfermieri");
			Object id = tabellaInfermieri.getValueAt(tabellaInfermieri
					.getSelectedRow(), tabellaInfermieri.getColumnModel()
					.getColumnIndex(colonne[0]));
			Object nome = tabellaInfermieri.getValueAt(tabellaInfermieri
					.getSelectedRow(), tabellaInfermieri.getColumnModel()
					.getColumnIndex(colonne[1]));
			Object cognome = tabellaInfermieri.getValueAt(tabellaInfermieri
					.getSelectedRow(), tabellaInfermieri.getColumnModel()
					.getColumnIndex(colonne[2]));

			// Setta il modulo per l'inserimento
			txID.setText(String.valueOf(id));
			txNome.setText(String.valueOf(nome));
			txCognome.setText(String.valueOf(cognome));
			txInfo.setText(Fc.MessageBundle("MsgDialog510"));
		}
	}

